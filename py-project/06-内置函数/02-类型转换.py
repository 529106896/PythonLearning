# ord 返回字符对应的ASCII码(只能有一个字符)
print(ord("A"))

# chr 返回十进制数（0-255）对应的ASCII字符
print(chr(65))

print(bool(56))
print(bool(-1))
print(bool(0))

# bin 转换为二进制
print(bin(255))

# hex 转换为十六进制
print(hex(10))

# dict 字典操作（经常用来创建字典）
myDict = dict(name='Kakyoin', age=18)
print(myDict)

# bytes转换为字节
print(bytes("珈乐今天吃嘉然", encoding='utf-8'))
